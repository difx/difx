#include <iostream>
#include <stdexcept>
#include <stdio.h>
#include <string>
#include <sstream>

/**
 * Execute a command
 *
 * @return Returns the stdout from the command execution
 **/
std::string execCommand(const char* cmd) {
    char buffer[128];
    std::string result = "";
    FILE* pipe = popen(cmd, "r");
    if (!pipe) throw std::runtime_error("popen() failed!");
    try {
        while (fgets(buffer, sizeof buffer, pipe) != NULL) {
            result += buffer;
        }
    } catch (...) {
        pclose(pipe);
        throw;
    }
    pclose(pipe);
    return result;
}

