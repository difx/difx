#ifndef __MATH_UTIL_H__
#define __MATH_UTIL_H__

/**
 * Return greatest common divisor.
 */
long long gcd(long, long);

/**
 * Return greatest common divisor.
 */
int gcd(int a, int b);

/**
 * Return greatest common divisor, with rounding of floating point input args.
 */
long long gcd(double, double);

#endif
